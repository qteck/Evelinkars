<h2>Being afraid of losing something</h2>
<p>My first book I read was Martin Eden written by Jack London. There is a ship in the harbor and young strong seaman walks around and helps a man who is assault by two unknown thieves. This man invite Martin in his house. He knocks on the door. A beautiful young woman is appeared and he immediately fall in love. Although she feels the same, it is not really easy to be together because of his origin. He hasn’t got any education and she comes from majority. He tries to get all the important staff to be with her and longs to get a writer. He does what is possible and goes through tough times. He get everything but he sees something what he didn’t expected he see the real world and painful truth.</p> 
<p>I read all the book and still was thinking about it. I started writing in the same style and topics. The Eden’s pages became a part of mine. </p>
<p>One day my friend asked me if I can lend him the book saying “It has got to be amazing story when you ‘re speaking about it so much”. I didn’t want because I loved it. It seemed to be half of me but despite the fact I lent it to him. During the time he was reading I was thinking if something wrong happens to it and was keeping asking about how is the book going. </p>
<p>He lost the book. He lost something what had discovered a part of mine which i hadn’t had no ideas before. I felt pain and sadness. It was mine. I loved it and he lost it. I couldn’t have even fallen asleep thinking where it could be.</p> 
<p>The pain wasn’t about possession. It was about feeling a connection and spirit of the book. I was just amazed by it and anything what is amazing it’s easy to fall in love with and being afraid of losing.</p>
<p>And now I have an amazing woman.</p>
